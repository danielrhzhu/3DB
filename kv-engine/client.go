package kv

import (
	"crypto/rand"
	"math/big"

	"3db/networking"
)

type Clerk struct {
	servers []*networking.ClientEnd
}

func nrand() int64 {
	max := big.NewInt(int64(1) << 62)
	bigx, _ := rand.Int(rand.Reader, max)
	x := bigx.Int64()
	return x
}

func MakeClerk(servers []*networking.ClientEnd) *Clerk {
	ck := new(Clerk)
	ck.servers = servers
	return ck
}

